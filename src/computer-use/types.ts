// Computer Use Task Types
export interface ComputerUseTask {
  id: string;
  description: string;
  type:
    | 'web_navigation'
    | 'desktop_operation'
    | 'coding_task'
    | 'research'
    | 'monitoring'
    | 'complex_workflow'
    | 'build_app'
    | 'test_app'
    | 'self_improve';
  status: 'pending' | 'in_progress' | 'completed' | 'failed';
  priority: 'low' | 'medium' | 'high' | 'critical';
  timeout?: number;
  context?: ComputerContext;
  subtasks?: ComputerUseTask[];
  dependencies?: string[];
  startedAt?: Date;
  completedAt?: Date;
  result?: any;
  error?: string;
  metadata?: Record<string, any>;
}

export interface ComputerContext {
  activeWindow?: string;
  openApplications?: string[];
  currentUrl?: string;
  screenState?: ScreenState;
  clipboard?: string;
  timestamp: Date;
}

export interface ScreenState {
  screenshot?: Buffer;
  activeElement?: UIElement;
  mousePosition?: { x: number; y: number };
  changedRegions?: Array<{ x: number; y: number; width: number; height: number }>;
}

export interface UIElement {
  name: string;
  controlType: string;
  boundingRectangle: { x: number; y: number; width: number; height: number };
  isEnabled: boolean;
  isOffscreen: boolean;
  children?: UIElement[];
  automationId?: string;
}

