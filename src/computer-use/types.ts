/**
 * Execution plan generated by TaskPlanner for complex autonomous tasks
 */
export interface ExecutionPlan {
  id: string;
  goal: string;
  steps: ExecutionStep[];
  estimatedDuration: number;
  requiredCapabilities: string[];
  status: 'draft' | 'approved' | 'executing' | 'completed' | 'failed';
  createdAt: Date;
  updatedAt: Date;
}

/**
 * Individual step in an execution plan
 */
export interface ExecutionStep {
  id: string;
  planId: string;
  order: number;
  type: 'research' | 'code' | 'test' | 'deploy' | 'browser' | 'monitor' | 'notify' | 'self_improve';
  description: string;
  tool: string;
  params: Record<string, any>;
  status: 'pending' | 'in_progress' | 'completed' | 'failed' | 'skipped';
  result?: any;
  error?: string;
  retryCount: number;
  maxRetries: number;
  startedAt?: Date;
  completedAt?: Date;
}

/**
 * Comprehensive execution log entry
 */
export interface ExecutionLogEntry {
  id: string;
  timestamp: Date;
  level: 'debug' | 'info' | 'warn' | 'error' | 'critical';
  category:
    | 'planning'
    | 'research'
    | 'coding'
    | 'testing'
    | 'browser'
    | 'deployment'
    | 'self_improvement'
    | 'notification';
  message: string;
  data?: Record<string, any>;
  taskId?: string;
  stepId?: string;
  planId?: string;
}

/**
 * Self-improvement capability definition
 */
export interface Capability {
  id: string;
  name: string;
  description: string;
  category: 'browser' | 'code' | 'api' | 'integration' | 'analysis';
  code: string; // Implementation code or MCP server config
  source: 'built_in' | 'user_created' | 'auto_generated';
  version: string;
  isActive: boolean;
  dependencies: string[];
  createdAt: Date;
  lastUsedAt?: Date;
}

/**
 * Goal notification payload
 */
export interface GoalNotification {
  goalId: string;
  goalDescription: string;
  status: 'completed' | 'failed' | 'needs_review';
  summary: string;
  details: {
    tasksCompleted: number;
    tasksFailed: number;
    duration: number;
    logs: ExecutionLogEntry[];
    artifacts: Artifact[];
  };
  timestamp: Date;
  userNotified: boolean;
}

/**
 * Artifact produced during execution (files, apps, reports)
 */
export interface Artifact {
  id: string;
  type: 'file' | 'application' | 'report' | 'screenshot' | 'log';
  name: string;
  path: string;
  metadata: Record<string, any>;
  createdAt: Date;
}

/**
 * Autonomous agent configuration
 */
export interface AutonomousAgentConfig {
  name: string;
  workspace: string;
  llmModel: string;
  visionModel?: string;
  maxConcurrentTasks: number;
  maxRetries: number;
  timeout: number;
  autoNotify: boolean;
  notificationChannels: ('websocket' | 'email' | 'slack')[];
  selfImprovementEnabled: boolean;
  browserConfig: {
    headless: boolean;
    defaultTimeout: number;
    screenshotOnFailure: boolean;
  };
}

/**
 * Agent state for persistence
 */
export interface AgentState {
  id: string;
  config: AutonomousAgentConfig;
  activePlanId?: string;
  capabilities: Capability[];
  stats: {
    tasksCompleted: number;
    tasksFailed: number;
    improvementsApplied: number;
    uptime: number;
    startedAt: Date;
  };
}

